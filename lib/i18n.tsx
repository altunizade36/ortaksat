type LanguageContextValue = {
  language: AppLanguage;
  setLanguage: (language: AppLanguage) => Promise<void>;
  useDeviceLanguage: () => Promise<void>;
  t: (key: keyof typeof dictionaries.tr) => string;
};

const LanguageContext = createContext<LanguageContextValue | undefined>(undefined);

const fallbackLanguageContext: LanguageContextValue = {
  language: "tr",
  async setLanguage() {
    return undefined;
  },
  async useDeviceLanguage() {
    return undefined;
  },
  t(key) {
    return dictionaries.tr[key] ?? key;
  }
};

function detectDeviceLanguage(): AppLanguage {
  const code = getLocales()[0]?.languageCode?.toLowerCase();
  return code === "en" ? "en" : "tr";
}

export function LanguageProvider({ children }: PropsWithChildren) {
  // Deterministic default ("tr") so the static-export HTML and the first client
  // render agree (avoids a hydration mismatch / React #418). The real device or
  // stored language is applied after mount, client-side only.
  const [language, setLanguageState] = useState<AppLanguage>("tr");

  useEffect(() => {
    // Dil ŞİMDİLİK DAİMA Türkçe. İngilizce arayüz gizlendi (buton/otomatik-algılama yok);
    // EN çeviri kodu duruyor, sonra açılacak. Cihaz İngilizce olsa bile TR gösterilir.
    setActiveLanguage("tr");
    setLanguageState("tr");
  }, []);

  // Web: <html lang>'i seçili dile göre güncelle + locale yardımcılarını (tarih/sayı)
  // aynı dile senkronla (erişilebilirlik + SEO + tutarlı biçimlendirme).
  useEffect(() => {
    setActiveLanguage(language);
    if (typeof document !== "undefined") {
      document.documentElement.lang = language;
    }
  }, [language]);

  const value = useMemo<LanguageContextValue>(() => ({
    language,
    async setLanguage(nextLanguage) {
      setActiveLanguage(nextLanguage);
      setLanguageState(nextLanguage);
      await AsyncStorage.setItem(STORAGE_KEY, nextLanguage);
    },
    async useDeviceLanguage() {
      const nextLanguage = detectDeviceLanguage();
      setActiveLanguage(nextLanguage);
      setLanguageState(nextLanguage);
      await AsyncStorage.removeItem(STORAGE_KEY);
    },
    t(key) {
      return dictionaries[language][key] ?? dictionaries.tr[key] ?? key;
    }
  }), [language]);

  return <LanguageContext.Provider value={value}>{children}</LanguageContext.Provider>;
}

export function useLanguage() {
  const context = useContext(LanguageContext);
  return context ?? fallbackLanguageContext;
}

