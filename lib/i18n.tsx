// Temel + tüm sayfa-grubu sözlükleri birleştirilmiş nihai TR→EN haritası.
const trToEnCopy: Record<string, string> = {
  ...baseTrToEn,
  ...homeDict,
  ...exploreDict,
  ...catalogDict,
  ...socialDict,
  ...componentsDict,
  ...components2Dict,
  ...categoriesDict,
  ...accountDict,
  ...profileDict,
  ...marketingDict,
  ...pass2Dict,
  ...extraDict
};

export function translateCopy(value: string, language: AppLanguage): string {
  if (language !== "en") return value;
  const direct = trToEnCopy[value.trim()];
  if (direct) return direct;
  return value
    .replace(/\bKazanç\b/g, "Earning")
    .replace(/\bKomisyon\b/g, "Commission")
    .replace(/\bSatış\b/g, "Sale")
    .replace(/\bTalep\b/g, "Lead")
    .replace(/\bBaşvuru\b/g, "Application")
    .replace(/\bBekleyen\b/g, "Pending")
    .replace(/\bAktif\b/g, "Active")
    .replace(/\bPasif\b/g, "Paused")
    .replace(/\bYeni\b/g, "New")
    .replace(/\bstok\b/g, "stock")
    .replace(/\bgün\b/g, "days")
    .replace(/\bsonuç\b/g, "results")
    .replace(/\biçerik\b/g, "items");
}
